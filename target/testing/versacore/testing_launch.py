#!/usr/bin/env python3
"""Launch gemm_mem_chip_1cluster tests from ``testing_workload.csv``."""

from __future__ import annotations

import argparse
import csv
import shutil
import subprocess
import sys
from pathlib import Path
from typing import Dict, Iterator

SCRIPT_DIR = Path(__file__).resolve().parent
TESTING_DIR = SCRIPT_DIR.parent
REPO_ROOT = SCRIPT_DIR.parents[2]
WORKLOAD_NAME = "gemm_mem_chip_1cluster"
DEV_APP = "snax-bingo-offload"
WORKLOAD_DIR = (
    REPO_ROOT
    / "target/sw/host/apps/offload_bingo_hw/single_chip/workloads"
    / WORKLOAD_NAME
)
PARAMS_PATH = (
    WORKLOAD_DIR
    / "params.hjson"
)
DEFAULT_WORKLOAD_CSV = SCRIPT_DIR / "testing_workload.csv"

CFG_NAME = "hemaia_multichip_ci.hjson"
SIM_CFG_NAME = "sim_rtl.hjson"

HOST_APP_TYPE = "offload_bingo_hw"
CHIP_TYPE = "single_chip"

sys.path.insert(0, str(TESTING_DIR))

PARAM_FIELDS = [
    "num_clusters",
    "array_shape",
    "data_type",
    "transposed_A",
    "transposed_B",
    "int4_a_enable",
    "int4_b_enable",
    "K",
    "N",
    "M",
    "addNonZeroC",
    "addZeroC",
    "accumPrevC",
    "quantization_enable",
    "int32tofp16_enable",
]


def int_value(value: object) -> int:
    if isinstance(value, int):
        return value
    return int(str(value).strip())


def row_to_params(row: Dict[str, str]) -> Dict[str, int]:
    return {field: int_value(row[field]) for field in PARAM_FIELDS if field in row}


def write_params_hjson(params: Dict[str, int], path: Path = PARAMS_PATH) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "// Auto-generated by target/testing/versacore/testing_launch.py",
        "{",
    ]
    for key in PARAM_FIELDS:
        if key in params:
            lines.append(f"  {key}: {params[key]},")
    lines.append("}")
    path.write_text("\n".join(lines) + "\n")


def iter_workload_csv(path: Path) -> Iterator[Dict[str, str]]:
    with path.open(newline="") as f:
        yield from csv.DictReader(f)


def cfg_override() -> str:
    return f"target/rtl/cfg/{CFG_NAME}"


def sim_cfg_path() -> Path:
    return REPO_ROOT / "target/sim/cfg" / SIM_CFG_NAME


def first_run_setup() -> None:
    from test_util import (
        DEFAULT_DOCKER_IMAGE,
        run_in_container,
        step0_reset,
        step1_init_private_modules,
    )

    if shutil.which("vcs") is None:
        print(
            "ERROR: 'vcs' not found on PATH.\n"
            "Please source the VCS setup script and re-run this script.",
            file=sys.stderr,
        )
        sys.exit(1)

    print(f"[Step 0] Running make clean under {REPO_ROOT}")
    step0_reset(REPO_ROOT)

    print("[Step 1] Initialising single-chip open-source module setup")
    step1_init_private_modules(
        REPO_ROOT,
        with_macro=True,
        with_d2d=True,
        with_pll=True,
    )

    print("[Step 2] Rebuilding SW/bootrom/RTL and preparing VCS inputs")
    run_in_container(
        REPO_ROOT,
        DEFAULT_DOCKER_IMAGE,
        REPO_ROOT,
        ["make", "sw", f"CFG_OVERRIDE={cfg_override()}"],
    )
    run_in_container(
        REPO_ROOT,
        DEFAULT_DOCKER_IMAGE,
        REPO_ROOT,
        ["make", "bootrom", f"CFG_OVERRIDE={cfg_override()}"],
    )
    run_in_container(
        REPO_ROOT,
        DEFAULT_DOCKER_IMAGE,
        REPO_ROOT,
        ["make", "rtl", f"CFG_OVERRIDE={cfg_override()}"],
    )
    run_in_container(
        REPO_ROOT,
        DEFAULT_DOCKER_IMAGE,
        REPO_ROOT,
        ["make", "hemaia_system_vcs_preparation", f"SIM_CFG={sim_cfg_path()}"],
    )

    print("[Step 3] Rebuilding VCS binary")
    subprocess.run(
        ["make", "hemaia_system_vcs", f"SIM_CFG={sim_cfg_path()}"],
        cwd=REPO_ROOT,
        check=True,
    )


def rebuild_sw() -> None:
    from test_util import DEFAULT_DOCKER_IMAGE, run_in_container

    print("[Per-test] Rebuilding SW")
    run_in_container(
        REPO_ROOT,
        DEFAULT_DOCKER_IMAGE,
        REPO_ROOT,
        ["make", "clean-sw"],
    )
    run_in_container(
        REPO_ROOT,
        DEFAULT_DOCKER_IMAGE,
        REPO_ROOT,
        [
            "make",
            "apps",
            f"CFG_OVERRIDE={cfg_override()}",
            f"HOST_APP_TYPE={HOST_APP_TYPE}",
            f"CHIP_TYPE={CHIP_TYPE}",
            f"WORKLOAD={WORKLOAD_NAME}",
            f"DEV_APP={DEV_APP}",
        ],
    )


def run_one_case(params: Dict[str, int]) -> None:
    print("[Per-test] Writing params.hjson")
    write_params_hjson(params)

    print("[Per-test] Rebuilding SW")
    rebuild_sw()

    print("[Per-test] Making sure VCS simulation binary exists")
    subprocess.run(
        ["make", "hemaia_system_vcs", f"SIM_CFG={sim_cfg_path()}"],
        cwd=REPO_ROOT,
        check=True,
    )

    print("[Per-test] Running VCS simulation binary")
    subprocess.run(
        ["./occamy_chip.vcs"],
        cwd=REPO_ROOT / "target/sim/bin",
        check=True,
    )


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--workload-csv", type=Path, default=DEFAULT_WORKLOAD_CSV)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    first_run_setup()
    for index, row in enumerate(iter_workload_csv(args.workload_csv)):
        if index == 0:
            params = row_to_params(row)
            test_name = row.get("test_name") or f"row_{index}"
            print(
                f"[{index}] {test_name}: "
                f"M={params['M']} K={params['K']} N={params['N']}"
            )
            run_one_case(params)


if __name__ == "__main__":
    main()
