# Data generator for the xdma_sweep RTL characterization workload.
# Emits one input buffer per config, sized to the config's in_bytes.
# (Correctness is verified against a single shared all-zero golden.)

import numpy as np


def compute_in_bytes(op: str, size: int, rows: int, cols: int, elem: int) -> int:
    if op == "copy_1d":
        return size
    if op in ("transpose_2d", "submatrix_2d"):
        return rows * cols * elem
    raise ValueError(f"unknown op: {op}")


def emit_header_file(configs: list) -> str:
    """Generate xdma_sweep_data.h with per-config input buffers.

    configs: list of (op, size, rows, cols, elem_bytes) tuples.
    """
    lines = [
        "// Auto-generated by xdma_sweep_datagen.py",
        "#pragma once",
        "#include <stdint.h>",
    ]
    max_bytes = 0
    for i, cfg in enumerate(configs):
        op, size, rows, cols, elem = cfg
        nbytes = compute_in_bytes(op, size, rows, cols, elem)
        arr = (np.arange(nbytes, dtype=np.uint8) & 0xFF)
        lines.append("")
        lines.append(f"// cfg{i}: op={op} size={size} rows={rows} cols={cols} elem={elem} ({nbytes} B)")
        lines.append(f"static uint8_t input_cfg{i}[{nbytes}] __attribute__((aligned(8))) = {{")
        for j in range(0, nbytes, 16):
            chunk = arr[j:j+16]
            lines.append("    " + ", ".join(str(int(v)) for v in chunk) + ",")
        lines.append("};")
        max_bytes = max(max_bytes, nbytes)
    # Shared all-zero golden for quick correctness check (we only verify the
    # first few output bytes against zeros, which is enough to catch kernel
    # dispatch failures).  Correctness of the op itself is covered by the
    # dedicated xdma_all_ops functional workload.
    lines.append("")
    lines.append(f"#define XDMA_SWEEP_MAX_BYTES {max_bytes}")
    return "\n".join(lines) + "\n"
