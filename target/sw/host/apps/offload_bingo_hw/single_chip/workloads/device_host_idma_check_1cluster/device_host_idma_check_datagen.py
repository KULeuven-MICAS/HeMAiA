"""Generate data for device_host_idma_check_1cluster."""

import numpy as np


def _byte_array(name, values):
    lines = [f"static uint8_t {name}[{values.size}] __attribute__((aligned(8))) = {{"]
    for i in range(0, values.size, 16):
        chunk = values[i:i + 16]
        lines.append("    " + ", ".join(str(int(v)) for v in chunk) + ",")
    lines.append("};")
    return lines


def emit_header_file(size):
    values = ((np.arange(size, dtype=np.uint16) * 13 + 7) & 0xFF).astype(np.uint8)
    lines = [
        "// Auto-generated by device_host_idma_check_datagen.py",
        "#pragma once",
        "#include <stdint.h>",
        "",
    ]
    lines += _byte_array("input_data", values)
    lines.append("")
    lines += _byte_array("golden_data", values)
    return "\n".join(lines) + "\n"
