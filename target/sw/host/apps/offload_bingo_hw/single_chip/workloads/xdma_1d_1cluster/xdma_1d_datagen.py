# Data generator for the xdma_1d single 1D-copy test: one input buffer and its
# golden. The xDMA 1d_copy is the identity, so golden == input.

import numpy as np


def _byte_array(name: str, arr: np.ndarray) -> list:
    lines = [f"static uint8_t {name}[{arr.size}] __attribute__((aligned(8))) = {{"]
    for j in range(0, arr.size, 16):
        chunk = arr[j:j + 16]
        lines.append("    " + ", ".join(str(int(v)) for v in chunk) + ",")
    lines.append("};")
    return lines


def emit_header_file(size: int) -> str:
    """Generate xdma_1d_data.h with input_data[size] and golden_copy[size]."""
    arr = (np.arange(size, dtype=np.uint8) & 0xFF)
    lines = [
        "// Auto-generated by xdma_1d_datagen.py",
        "#pragma once",
        "#include <stdint.h>",
        "",
    ]
    lines += _byte_array("input_data", arr)
    lines.append("")
    lines += _byte_array("golden_copy", arr)   # copy is identity
    return "\n".join(lines) + "\n"
