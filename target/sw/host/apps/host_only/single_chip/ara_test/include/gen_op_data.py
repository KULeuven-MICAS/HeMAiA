#!/usr/bin/env python3
"""Generate test data for the ara_test timing sweep.

Uses a seeded PRNG for reproducible but non-trivial input patterns.

Emits only the large (N_BIG=8192) arrays needed by the multi-size timing
sweep in ara_test/src/main.c.  The correctness checks with their golden
values live in the paired ci_ara workload.
"""
import random

N_BIG = 8192   # max size for timing sweep (covers TinyLlama range)
SEED = 42

def main():
    rng = random.Random(SEED)

    print("// Auto-generated by gen_op_data.py (seed=%d)" % SEED)
    print("#pragma once")
    print(f"#define OP_MAX_LEN {N_BIG}")
    print()

    # op_a_big: positive values in [0.3, 4.0] — safe for exp/sqrt/reciprocal
    a_big = [round(rng.uniform(0.3, 4.0), 4) for _ in range(N_BIG)]
    # op_b_big: positive values in [0.5, 3.0] — avoids div-by-near-zero
    b_big = [round(rng.uniform(0.5, 3.0), 4) for _ in range(N_BIG)]
    # op_mixed_big: values in [-5, 5] — exercises sign handling
    mixed_big = [round(rng.uniform(-5.0, 5.0), 4) for _ in range(N_BIG)]

    # Large arrays for timing sweeps. Placed in .data with 8-byte alignment
    # for vector loads.
    def emit_big(name, values):
        print(f"static float {name}[{N_BIG}] __attribute__((aligned(8))) = {{")
        # Emit 8 values per line to keep the file compact
        for i in range(0, N_BIG, 8):
            chunk = values[i:i+8]
            print("  " + ", ".join(f"{v:.6f}f" for v in chunk) + ",")
        print("};")

    emit_big("op_a_big", a_big)
    emit_big("op_b_big", b_big)
    emit_big("op_mixed_big", mixed_big)

main()
