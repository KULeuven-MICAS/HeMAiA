#!/usr/bin/env python3
"""Generate test data for FP32 operator correctness tests (ci_ara).

Uses a seeded PRNG for reproducible but non-trivial input patterns.
Only emits the small N=32 arrays + golden values; the large arrays used
for the timing sweep live in the paired ara_test workload.
"""
import math
import random

N = 32         # correctness-check vector length
SEED = 42

def main():
    rng = random.Random(SEED)

    print("// Auto-generated by gen_op_data.py (seed=%d)" % SEED)
    print("#pragma once")
    print(f"#define OP_TEST_LEN {N}")
    print()

    # Vector inputs go through Ara, so they must be aligned to the lane stride
    # (4 bytes per fp32 element × NR_LANES).  NR_LANES is supplied at compile
    # time via -DNR_LANES (set by target/sw/host/apps/common.mk).
    ALIGN = "__attribute__((aligned(4 * NR_LANES)))"

    # op_a: positive values in [0.3, 4.0] — safe for exp/sqrt/reciprocal
    a_vals = [round(rng.uniform(0.3, 4.0), 4) for _ in range(N)]
    # op_b: positive values in [0.5, 3.0] — avoids div-by-near-zero
    b_vals = [round(rng.uniform(0.5, 3.0), 4) for _ in range(N)]
    # op_mixed: values in [-5, 5] — exercises sign handling for relu/abs/sigmoid/tanh
    mixed = [round(rng.uniform(-5.0, 5.0), 4) for _ in range(N)]

    print(f"static float op_a[{N}] {ALIGN} = {{")
    print("  " + ", ".join(f"{v:.6f}f" for v in a_vals))
    print("};")
    print(f"static float op_b[{N}] {ALIGN} = {{")
    print("  " + ", ".join(f"{v:.6f}f" for v in b_vals))
    print("};")
    print(f"static float op_mixed[{N}] {ALIGN} = {{")
    print("  " + ", ".join(f"{v:.6f}f" for v in mixed))
    print("};")

    # Golden values for binary ops
    for name, op in [("add", lambda a,b: a+b), ("sub", lambda a,b: a-b),
                     ("mul", lambda a,b: a*b), ("div", lambda a,b: a/b),
                     ("max", lambda a,b: max(a,b)), ("min", lambda a,b: min(a,b))]:
        golden = [op(a, b) for a, b in zip(a_vals, b_vals)]
        print(f"static float golden_{name}[{N}] = {{")
        print("  " + ", ".join(f"{v:.6f}f" for v in golden))
        print("};")

    # Golden values for unary ops
    for name, op in [
        ("relu", lambda x: max(0, x)),
        ("neg", lambda x: -x),
        ("abs", lambda x: abs(x)),
        ("exp", lambda x: math.exp(min(x, 20))),
        ("sigmoid", lambda x: 1.0 / (1.0 + math.exp(-x))),
        ("tanh", lambda x: math.tanh(x)),
        ("sqrt", lambda x: math.sqrt(x)),        # op_a is always positive
        ("reciprocal", lambda x: 1.0 / x),       # op_a is always > 0.3
        ("silu", lambda x: x / (1.0 + math.exp(-x))),
    ]:
        inp = a_vals if name in ("exp", "sqrt", "reciprocal") else mixed
        golden = [op(v) for v in inp]
        print(f"static float golden_{name}[{N}] = {{")
        print("  " + ", ".join(f"{v:.6f}f" for v in golden))
        print("};")

    # Golden values for reductions (on a_vals)
    print(f"static float golden_reduce_sum = {sum(a_vals):.6f}f;")
    print(f"static float golden_reduce_max = {max(a_vals):.6f}f;")
    print(f"static float golden_reduce_mean = {sum(a_vals)/N:.6f}f;")

main()
